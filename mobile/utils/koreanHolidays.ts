export function getKoreanHolidayName(dateKey: string): string | null {
  const monthDay = dateKey.slice(5);
  const fixedName = FIXED_HOLIDAY_NAMES[monthDay];
  if (fixedName) {
    return fixedName;
  }

  const year = Number(dateKey.slice(0, 4));
  return HOLIDAY_NAMES_BY_YEAR[year]?.[dateKey] ?? null;
}

export function isKoreanHoliday(dateKey: string): boolean {
  return getKoreanHolidayName(dateKey) !== null;
}

/** 일요일 또는 공휴일 — 달력 빨간색 표시용 */
export function isRedCalendarDay(dateKey: string, dayOfWeek: number): boolean {
  return dayOfWeek === 0 || isKoreanHoliday(dateKey);
}
